    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jtxtEnter1.getText().equals("") || jtxtEnter2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You Must Enter Value To Compute This Progress",
                    "Calculator System", JOptionPane.ERROR_MESSAGE);
        } else {
            double num1, num2, num3 = 0;
            num1 = Double.parseDouble(jtxtEnter1.getText());
            num2 = Double.parseDouble(jtxtEnter2.getText());
            Object select = jcbSelect.getSelectedItem();
            if (select.equals("Distance")) {
                num3 = num1 * num2;
                jlabDisplay1.setText("Distance=");
                jlabDisplay2.setText("Speed");
                jlabDisplay3.setText("*");
                jlabDisplay4.setText("Time=");
            } else if (select.equals("Speed")) {
                num3 = num1 / num2;
                jlabDisplay1.setText("Speed=");
                jlabDisplay2.setText("Distance");
                jlabDisplay3.setText("/");
                jlabDisplay4.setText("Time=");
            } else if (select.equals("Time")) {
                num3 = num1 / num2;
                jlabDisplay1.setText("Time=");
                jlabDisplay2.setText("Distance");
                jlabDisplay3.setText("/");
                jlabDisplay4.setText("Speed=");
            } else {
                JOptionPane.showMessageDialog(null, "You Must Enter Your Select Operation To Compute This Progress",
                        "Calculator System", JOptionPane.ERROR_MESSAGE);
            }
            jlabResult.setText(String.valueOf(num3));

        }

    }//GEN-LAST:event_jButton2ActionPerformed

