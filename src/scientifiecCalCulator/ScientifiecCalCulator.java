    public void doEqual() {
        
        secondNum = Double.parseDouble(jtxtShow.getText());
        result = secondNum;
        switch (operation) {
            case '+':
                result = firstNum + secondNum;
                break;

            case '-':
                result = firstNum - secondNum;
                break;

            case '*':
                result = firstNum * secondNum;
                break;

            case '/':
                result = firstNum / secondNum;
                break;
            case '%':
                result = firstNum % secondNum;
                break;
            case '^':
                result = Math.pow(firstNum, secondNum);
        }
        operation = '\u0000';
        jtxtShow.setText(String.valueOf(result));
    }


