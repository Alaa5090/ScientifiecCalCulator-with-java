    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jtxtEnter.getText().equals("")) 
            JOptionPane.showMessageDialog(null, "You Must Enter Value To Compute This Progress",
                    "Calculator System", JOptionPane.ERROR_MESSAGE);

        else{
            double pound = Double.parseDouble(String.valueOf(jtxtEnter.getText()));
            String resultConvert = "";
            switch (jcbCurrency.getSelectedIndex()) {
                case 1:
                    resultConvert = "$ ".concat(String.valueOf(pound * currencyOfUSA));
                    break;
                case 2:
                    resultConvert = "EUR ".concat(String.valueOf(pound * currencyOfGerman));
                    break;
                case 3:
                    resultConvert = "$R ".concat(String.valueOf(pound * currencyOfBrazil));
                    break;
                case 4:
                    resultConvert = "C$ ".concat(String.valueOf(pound * currencyOfCanada));
                    break;
                case 5:
                    resultConvert = "Rp ".concat(String.valueOf(pound * currencyOfIndonesia));
                    break;
                case 6:
                    resultConvert = "INR‎ ".concat(String.valueOf(pound * CurrencyOfIndia));
                    break;
                case 7: 
                    resultConvert = "SAR ".concat(String.valueOf(pound * CurrencyOfSaudi));
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "You Must Enter Your Select Operation To Compute This Progress",
                        "Calculator System", JOptionPane.ERROR_MESSAGE);
            }
            jlabConvert.setText(resultConvert);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

